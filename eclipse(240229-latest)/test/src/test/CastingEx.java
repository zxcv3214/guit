package test;

public class CastingEx {

	public static void main(String[] args) {
		// 자동형변환
		int a = 10;
		long b = a;
		System.out.println(b);
		
		// 강제형변환
		double c = 10.5;
		int d = (int)c;
		System.out.println(d);
		
		// 연산식
		int e = 10;
		double f = 1.5;
		double g=e+f;
		System.out.println(g);
		

	}

}
