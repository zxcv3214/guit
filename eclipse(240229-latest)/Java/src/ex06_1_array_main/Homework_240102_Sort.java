package ex06_1_array_main;

import java.util.Arrays;

public class Homework_240102_Sort {
	public static void main(String[] args) {

///////////////////////방법1/////////////////////////		
		
		int[] a = { 5, 4, 3, 2, 1 };
		int imsi = 0;

		for (int i = 0; i < 4; i++) {
			for (int j = 4; i < j; j--) {
				if (a[i] > a[j]) {
					imsi = a[i];
					a[i] = a[j];
					a[j] = imsi;
				}
			}
		}
		System.out.println(Arrays.toString(a));

		
///////////////////////방법2/////////////////////////		
		
		for (int i = 0; i < 4; i++) {
			for (int j = 4; j > 0 && 4 - j < j; j--) {
				if (a[i] > a[j]) {
					imsi = a[i];
					a[i] = a[j];
					a[j] = imsi;
				}
			}
		}
		System.out.println(Arrays.toString(a));
	}

}
