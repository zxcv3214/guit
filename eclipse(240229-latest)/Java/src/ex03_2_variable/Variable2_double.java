//기본형 변수의 값을 출력
package ex03_2_variable;
public class Variable2_double {
	public static void main(String[] args) {
		
		System.out.println("======실수형 변수======");
		//실수형 변수
		//f나 F를 뒤에 붙임
		float ft = 3.1F;
		
		//d나 D를 숫자 뒤에 붙이거나 생략할 수 있음
		double d = 42.195;
		
		System.out.println("f=" + ft + " d=" + d);
	}

}
